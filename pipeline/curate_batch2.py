# 2026-07 課號重編（四軌化）：CURATION 內寫的是舊課號，輸出時換成新課號。
REMAP = {"A1": "B1", "A2": "B2", "A3": "B3", "A4": "B4", "A5": "B5", "A6": "B6",
         "B1": "C1", "B2": "C2", "B3": "C3", "B4": "C4",
         "B5": "C5", "B6": "C6", "B7": "C7", "B8": "C8"}


def main() -> None:
    ANN.mkdir(parents=True, exist_ok=True)
    n = 0
    for pid, (concepts, teach, label, bbox, prompt, note) in CURATION.items():
        ann = {
            "photo": pid,
            "usable": True,
            "teachability": teach,
            "concepts": [REMAP.get(c, c) for c in concepts],
            "safety": {"brands": False, "text": False, "risky": False},
            "subject": {"label": label, "bbox": [round(v, 3) for v in bbox]},
            "kid_prompt": prompt,
            "curator_note": note,
        }
        (ANN / f"{pid}.json").write_text(
            json.dumps(ann, ensure_ascii=False, indent=2), encoding="utf-8"
        )
        n += 1
    print(f"批次二標註完成：寫入 {n} 筆 annotations")


if __name__ == "__main__":
    main()
